package com.technews;

public class VariableTypes {
    public static void main(String[] args) {
        //String keyword indicates a string of characters
        String name = "Cleopatra";
        //int keyword indicates whole numbers (or integers, like 1, 2, or 3)
        int age = 2016 + 69;
        //double data type stores decimal values (like 1.2, 2.3, or 3.54)
        double price = 1999.99;
        //boolean data type stores Boolean values (either true or false)
        boolean truth = true;
        boolean falseness = !truth;
    }
}
