package com.technews;

//When you create a class, the name of the file must correspond to the name of the class in your code
public class HelloWorld {
    //At least one class in every Java program needs a main() method,
    //which serves as an entry point for the program and gets everything started
    public static void main(String[] args){ //this method accepts a string array of arguments when the program first starts
        //println() belongs to the System.out object in Java and is used to print messages to the console
        System.out.println("Hello, World!");
    }
    // IntelliJ IDEA, right-click HelloWorld.java, then select "Run HelloWorld.main()"
    // similar to JavaScript's 'npm run'
}
